numero1 = int(input("Dame un numero\n"))
numero2 = int(input("Dame otro numero\n"))

resultado = numero1 + numero2
print(f"La suma de {numero1} y {numero2} es : {resultado}")

resultado = numero1 - numero2
print(f"La resta de {numero1} y {numero2} es : {resultado}")

resultado = numero1 * numero2
print(f"La multiplicacion de {numero1} y {numero2} es : {resultado}")

resultado = numero1 / numero2
print(f"La division de {numero1} y {numero2} es : {resultado}")

resultado = numero1 // numero2
print(f"La division entera de {numero1} y {numero2} es : {resultado}")

resultado = numero1 % numero2
print(f"El resto de la division entre {numero1} y {numero2} es : {resultado}")

resultado = numero1 ** numero2
print(f"La potencia de {numero1} a {numero2} es : {resultado}")
